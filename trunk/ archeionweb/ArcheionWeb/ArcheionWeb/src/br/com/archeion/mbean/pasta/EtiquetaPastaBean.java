package br.com.archeion.mbean.pasta;

public class EtiquetaPastaBean {
	private String empresa;
	private String local;
	private String itemDocumental;
	private String titulo;
	private String caixaOuCaixeta;
	private String previsaoRecolhimento;
	private String previsaoExpurgo;
	
	public String getEmpresa() {
		return empresa;
	}
	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	public String getItemDocumental() {
		return itemDocumental;
	}
	public void setItemDocumental(String itemDocumental) {
		this.itemDocumental = itemDocumental;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getCaixaOuCaixeta() {
		return caixaOuCaixeta;
	}
	public void setCaixaOuCaixeta(String caixaOuCaixeta) {
		this.caixaOuCaixeta = caixaOuCaixeta;
	}
	public String getPrevisaoRecolhimento() {
		return previsaoRecolhimento;
	}
	public void setPrevisaoRecolhimento(String previsaoRecolhimento) {
		this.previsaoRecolhimento = previsaoRecolhimento;
	}
	public String getPrevisaoExpurgo() {
		return previsaoExpurgo;
	}
	public void setPrevisaoExpurgo(String previsaoExpurgo) {
		this.previsaoExpurgo = previsaoExpurgo;
	}

}
